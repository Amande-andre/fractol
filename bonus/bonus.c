#include "../bonus_inc/fractol_bonus.h"

int	ft_julia_loop(t_data *f)
{
	f->c.re = -1;
	while (f->c.re < 1)
	{
		f->c.re += 0.02;
		while (f->c.im < 1)
		{
			f->c.im += 0.02;
			ft_print_julia(f);
		}
		f->c.im = -1.1;
	}
	return (0);
}

